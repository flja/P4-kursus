#include <stdio.h>

int main()
{
    //A
    int x = 0x87654321;
    x &= 0xFF;
    printf("x: %02x\n", x);
    //x =           1000 0111 0110 0101 0100 0011 0010 0001
    //0xFF =        0000 0000 0000 0000 0000 0000 1111 1111 
    //x & 0xFF =    0000 0000 0000 0000 0000 0000 0010 0001


    //B
    x = 0x87654321;
    x ^= ~0xFF;
    printf("x: %02x\n", x);
    //x =           1000 0111 0110 0101 0100 0011 0010 0001
    //~0xFF =       1111 1111 1111 1111 1111 1111 0000 0000
    //x ^(~0xFF) =  0111 1000 1001 1010 1011 1100 0010 0001


    //C
    x = 0x87654321;
    x |= 0xFF;
    //x =           1000 0111 0110 0101 0100 0011 0010 0001
    //0xFF =        0000 0000 0000 0000 0000 0000 1111 1111
    //x |= 0xFF =   1000 0111 0110 0101 0100 0011 1111 1111
    printf("x: %02x\n", x);
}
